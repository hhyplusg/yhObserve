package com.wave.core.plugin.mybatis;

import com.wave.base.vo.Vo;

public class PageVo extends Vo   { 
	/**
	 * serialVersionUID = -7485198726433785797L;
	 */
	private static final long serialVersionUID = -7485198726433785797L;
	
	private int pagesize = 10; //每页显示记录数 
	private int totalResultsCount;//总记录数
	private int pagenum;	//当前页 
	public int getPagesize() {
		return pagesize;
	}
	public void setPagesize(int pagesize) {
		this.pagesize = pagesize;
	}
	public int getTotalResultsCount() {
		return totalResultsCount;
	}
	public void setTotalResultsCount(int totalResultsCount) {
		this.totalResultsCount = totalResultsCount;
	}
	public int getPagenum() {
		return pagenum;
	}
	public void setPagenum(int pagenum) {
		this.pagenum = pagenum;
	}
	@Override
	public String toString() {
		return "PageVo [pagesize=" + pagesize + ", totalResultsCount="
				+ totalResultsCount + ", pagenum=" + pagenum + "]";
	}
	
   
}
